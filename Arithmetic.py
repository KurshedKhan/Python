# Arithmetic Operator
# Addition  ==> (+)

num1 = 10
num2 = 20

print("Addition Operator : ",num1 + num2)

# Note : String also use in addition ==> concatenate
str1 = "Hello"
str2 = "World"
str3 = " " #empty string

print(str1+str3+str2)

# Subtraction ==> (-)
print('Subtraction :',num2-num1)


#Multiplication ==> (*)
print("Muliplication : ",num1*num2)

# Note : String also use in multiplication ==> Repeation
print(str1*2)
print(str2*3)
print(str1*2)

#Division ==> (/) ==> Quotient
num1 = 15 
num2 = 2
print("Division :",num1/num2)

#Modular Division / Modulus ==> (%) ==> remainder
num1 = 25
num2 = 4
print("Modular Division : ",num1%num2)

#Floor Division/Integer Division ==> (//) ==> only given integer number, remove decimal number
num1 = 15
num2 = 2
print("Floor Division : ",num1//num2)


#Exponential (Power) ==> (**)
num1 = 5
num2 = 3
print("Exponential : ",num1**num2)



